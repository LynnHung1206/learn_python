# 次方 x 的 y 次方
x = 2
y = 3
a = x ** y
z = 0.5
b = x ** z
print(a)
print(b)

# 除法
x = 3 / 6
y = 3 // 6  # 整數除法 不會有小數
print(x)
print(y)

a1 = 2
a1 *= 2
print(a1)

# 字串
s = "Hello"
s1 = 'Hi hi'
print(s + s1)
s2 = "Hello" 'Hi hi'  # 空白可以表示字串串接

print(s * 3)  # 可以重複三次字串

# 換行
s3 = "Hello \n World"
s4 = """Hello
World"""  # 一定要三個才可以換行

# 字元操作
print(s[0])
print(s[0:3])  # 包含開頭不包含結尾
print(s[3:])
print(s[:3])

